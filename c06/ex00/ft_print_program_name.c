#include <unistd.h>

void	ft_put_str(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int ac, char **av)
{
	(void)ac;
	ft_put_str(av[0]);
	write(1, "\n", 1);
	return (0);
}

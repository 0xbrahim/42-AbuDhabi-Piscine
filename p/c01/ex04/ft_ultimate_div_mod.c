void	ft_ultimate_div_mod(int *a, int *b)
{
	int	temp_a;

	if (*b != 0)
	{
		temp_a = *a;
		*a = temp_a / *b;
		*b = temp_a % *b;
	}
	else
	{
		*a = 0;
		*b = 0;
	}
}
